/* Write a program to print given pattern*/

class Patternprint
{
	public static void main(String[] args)
	{
		int a,b,c;
		for(a=5;a<=5;a++)
			
		{
			for(b=4;b<a;b++)
			
				
			{
			System.out.println("* * * * * ");
			System.out.println("* * * * ");
		}
		for(c=5;c<=a;c++)
		{
			System.out.println("* * *  ");
		}
		System.out.println("*");
				
		}
	}
}
